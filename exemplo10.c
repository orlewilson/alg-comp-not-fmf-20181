// Bibliotecas
#include <stdio.h> 

// Programa Principal
int main()
{
	// declarando vetor unidimensional
	int vetorA[5];

	// declarando vetor bidimensional
	int vetorB[2][2];

	// povoando valores nos vetores
	vetorA[0] = 7;
	vetorA[1] = 9;
	vetorA[2] = 11;
	vetorA[3] = 5;
	vetorA[4] = 12;
	
	vetorB[0][0] = 13;
	vetorB[0][1] = 1;
	vetorB[1][0] = 33;
	vetorB[1][1] = 93;
	
	// mostrar valores na tela
	printf("vetorA[0] = %d \n", vetorA[0]);
	printf("vetorA[1] = %d \n", vetorA[1]);
	printf("vetorA[2] = %d \n", vetorA[2]);
	printf("vetorA[3] = %d \n", vetorA[3]);
	printf("vetorA[4] = %d \n", vetorA[4]);
	
	printf("vetorB[0][0] = %d \n", vetorB[0][0]);
	printf("vetorB[0][1] = %d \n", vetorB[0][1]);
	printf("vetorB[1][0] = %d \n", vetorB[1][0]);
	printf("vetorB[1][1] = %d \n", vetorB[1][1]);
	
	
	
	
	
	
	
}








